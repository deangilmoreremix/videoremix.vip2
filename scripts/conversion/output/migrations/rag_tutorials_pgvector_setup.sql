-- ============================================================
-- Function: match_documents(vector, integer, float)
-- ============================================================
-- Searches the 'documents' table (created by Supabase pgvector plugin)
-- returning the k most similar documents with similarity score.
--
-- Parameters:
--   query_embedding  - vector (1536 dimensions expected)
--   match_threshold  - minimum cosine similarity (default 0.0)
--   match_count      - maximum number of documents to return (default 5)
--
-- Returns: id, content, metadata, similarity (1 - distance)
-- ============================================================

create or replace function match_documents (
  query_embedding vector(1536),
  match_threshold float default 0.0,
  match_count int default 5
)
returns table (
  id bigint,
  content text,
  metadata jsonb,
  similarity float
)
language sql stable
as $$
  select
    documents.id,
    documents.content,
    documents.metadata,
    1 - (documents.embedding <=> query_embedding) as similarity
  from documents
  where 1 - (documents.embedding <=> query_embedding) > match_threshold
  order by documents.embedding <=> query_embedding
  limit match_count;
$$;

comment on function match_documents(vector, float, int) is
  'Vector similarity search over stored document embeddings. '
  ||'Returns the k-nearest neighbours with cosine similarity scores. '
  ||'Requires the pgvector extension and documents table with embedding column.';

-- ============================================================
-- Collections table to track multiple RAG collections (apps)
-- ============================================================
create table if not exists rag_collections (
  id bigint generated by default as identity primary key,
  slug text unique not null,
  name text not null,
  description text,
  embedding_model text not null default 'text-embedding-3-small',
  vector_size integer not null default 1536,
  distance_metric text not null default 'cosine',
  search_k integer not null default 5,
  score_threshold float not null default 0.0,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable RLS
alter table rag_collections enable row level security;

