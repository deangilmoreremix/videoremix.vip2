-- ============================================================
-- Helper: upsert collection record
-- ============================================================
create or replace function upsert_rag_collection(
  p_slug text,
  p_name text,
  p_description text default null,
  p_embedding_model text default 'text-embedding-3-small',
  p_vector_size integer default 1536,
  p_distance_metric text default 'cosine',
  p_search_k integer default 5,
  p_score_threshold float default 0.0
)
returns jsonb
language plpgsql
as $$
declare
  v_id bigint;
begin
  insert into rag_collections
    (slug, name, description, embedding_model, vector_size, distance_metric, search_k, score_threshold)
  values
    (p_slug, p_name, p_description, p_embedding_model, p_vector_size, p_distance_metric, p_search_k, p_score_threshold)
  on conflict (slug) do update set
    name = excluded.name,
    description = excluded.description,
    embedding_model = excluded.embedding_model,
    vector_size = excluded.vector_size,
    distance_metric = excluded.distance_metric,
    search_k = excluded.search_k,
    score_threshold = excluded.score_threshold,
    updated_at = timezone('utc'::text, now())
  returning id into v_id;

  return jsonb_build_object(
    'id', v_id,
    'slug', p_slug,
    'action', case when pg_last_notice() is null then 'inserted' else 'updated' end
  );
end;
$$;

-- ============================================================
-- RPC: get_rag_collection(slug)
-- ============================================================
create or replace function get_rag_collection(p_slug text)
returns jsonb
language sql stable
as $$
  select to_jsonb(rag_collections.*) from rag_collections
  where slug = p_slug
  limit 1;
$$;

-- ============================================================
-- RPC: list_rag_collections()
-- ============================================================
create or replace function list_rag_collections()
returns jsonb
language sql stable
as $$
  select jsonb_agg(to_jsonb(rag_collections))
  from rag_collections
  order by created_at desc;
$$;

